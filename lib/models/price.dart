class Price {
  Price(
    this.id,
    this.name,
    this.symbol,
    this.sector,
    this.open,
    this.close,
    this.percentageChange,
    this.volume,
    this.value,
    this.balanceSheet,
    this.marketCap,
    this.weight,
    this.logo,
  );

  final int id;

  final String name;

  final String symbol;

  final String sector;

  final double open;

  final double close;

  final double percentageChange;

  final double volume;

  final double value;

  final double balanceSheet;

  final double marketCap;

  final double weight;

  final String logo;
}
